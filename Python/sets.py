#Operations with Sets///

set1 = {1, 2, 3}
set2 = {3, 4, 5}

union_set = set1.union(set2)        #Union///
print(union_set)

inter_set = set1.intersection(set2)     #Intersection///
print(inter_set)


diff_set =set1.difference(set2)          #Difference///
print(diff_set)